package com.upgrad.addressbook;

/**
 *
 * @author IDC-205
 */
public class Record {
String name;  
long number; 

    public Record(String name, long number) {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public long getNumber() {
        return number;
    }

    
}
